<font size="6"><p><center>¿Por qué se encuentran en peligro de extincion la  ballena azul?</center></p></font>
<font size="4"><center><p>Las Ballenas son mamíferos acuáticos y vienen de la familia de los Cetáceos. Otros Cetáceos son los delfines, las marsopas y los cachalotes.</p>

<p>Las ballenas son animales de un muy gran tamaño y la Ballena Azul es la más grande de todos los animales ya que mide 30 metros de longitud.</p>

</p>Las ballenas tienen la piel lisa y tienen debajo de ella una capa de grasa como si fuera ropa térmica. Las ballenas pueden nadar gracias a su aleta caudal que está puesta horizontalmente. No tienen dientes y su boca en lugar de dientes tiene unas láminas o barbas, con las cuales puede filtrar el plancton o el krill.
Las ballenas se cazan porque se aprovecha todo por ejemplo: de su grasa se obtiene aceite y los huesos o su carne para la comida por esta razón, han sufrido una caza excesiva que ha llevado a muchas especies al borde de la extinción.<p>
<p>Una de las ballenas que están en peligro de extinción en América y en Asia es la Ballena Azul.
Se han encontrado ballenas azules de hasta 33 metros de longitud y su peso de 190 toneladas, pero normalmente son de 25 metros de longitud. Las ballenas azules hembras son un poquito más grandes que los machos. La Ballena Azul, como su nombre lo dice, es azul pero su panza o vientre grisáceo o amarillento. Esta ballena es fácil de confundir con la Ballena Rorcual ya que es muy parecida, en su forma pero es diferente en tamaño y su color de piel es de un gris oscuro o negro.</p></center></font>
