<font size="6"><p><center>¿Por qué se encuentran en peligro de extincion el  rinoceronte?</center></p></font>
<font size="4"><center><p>Los rinocerontes alguna vez habitaron libremente muchos lugares en Eurasia y África, y eran conocidos por los primeros europeos quienes los representaban en pinturas rupestres. A principios del siglo XX, se estima que había alrededor de 500,000 rinocerontes en África y Asia. En 1970, la población disminuyó a 70,000 pero actualmente solo quedan unos 27,000 rinocerontes en la naturaleza. Debido a la constante caza furtiva y la pérdida de hábitat, hoy en día sobreviven muy pocos rinocerontes fuera de los parques nacionales y las reservas. Dos especies de rinocerontes en Asia -el de Java y el de Sumatra- están en crítico peligro de extinción. Una subespecie del rinoceronte de Java fue declarada extinta en Vietnam en 2011. Sin embargo una pequeña población del rinoceronte de Java aun se aferra a la supervivencia en la isla indonesia de Java. Los exitosos esfuerzos de conservación han ayudado a la tercer especie asiática, el rinoceronte indio, a aumentar el número de su población. Su estado cambió de Amenazado a Vulnerable, pero la especie aun sigue siendo cazada furtivamente sola para obtener su cuerno.</p>

<p>En África, los rinocerontes blancos del sur, que una vez se creyeron extintos, ahora prosperan en santuarios protegidos y están clasificados como Casi Amenazados. Pero el rinoceronte negro occidental y los rinocerontes blancos del norte se extinguieron recientemente en su hábitat natural. Los únicos tres rinocerontes blancos del norte que quedan se mantienen bajo vigilancia las 24 horas en Ol Pejeta Conservancy, en Kenia. Los rinocerontes negros se han duplicado en las últimas dos décadas desde que la población llegó a ser tan solo de 2,500 individuos, pero el número total sigue siendo solo una porción de los 100,000 rinocerontes que se calcula existían en la primera parte del siglo XX.</p></center></font>
