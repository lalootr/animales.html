<font size="6"><p><center>¿Por qué se encuentran en peligro de extincion los leopardo de las nieves?</center></p></font>
<font size="4"><center>    El leopardo de las nieves ha evolucionado para poder sobrevivir bajo las condiciones más extremas de la Tierra. Su pelaje blanco grisáceo con grandes manchas negras combina perfectamente con la textura de las escarpadas y rocosas montañas de Asia Central.

La poderosa anatomía del leopardo de las nieves le permite escalar grandes y empinadas pendientes con facilidad. Sus patas traseras le dan al leopardo de las nieves la capacidad de saltar el equivalente a seis veces la longitud de su cuerpo. Su larga cola le proporciona equilibrio y agilidad, y también le permite al leopardo de las nieves cobijarse del frío cuando se encuentra en reposo.

Durante miles de años, este magnífico felino fue el rey de las montañas, en las cuales abundaban presas como cabras azules, argalíes, íbices, marmotas, conejos de roca y liebres. El leopardo de las nieves se encuentra en 12 países, incluyendo China, Bután, Nepal, India, Pakistán, Afganistán, Rusia y Mongolia, pero su población está disminuyendo


<p>ASESINATOS COMO REPRESALIA</p>

En ocasiones, los leopardos de las nieves se alimentan ovejas, cabras, caballos y crías de kay que forman parte del ganado de los granjeros locales, quienes en represalia matan a los leopardos.

Pero los habitantes también cazan a los animales que los leopardos de las nieves necesitan para sobrevivir, como los argalíes. A medida que su presa natural se vuelve más difícil de encontrar, el leopardo de las nieves se ve obligado a matar ganado para sobrevivir.

<p>FRAGMENTACIÓN DEL HÁBITAT</p>

Conforme los asentamientos humanos crecen y se destinan más áreas para el pastoreo, el rango del hábitat del leopardo de las nieves continúa disminuyendo. Ese desarrollo fragmenta cada vez más el rango en el que históricamente esta especie ha habitado.

