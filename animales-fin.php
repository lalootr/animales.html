<font size="6"><p><center>¿Por qué se encuentran en peligro de extincion el Delfín Baiji?</center></p></font>
<font size="4"><center>Científicos de la Sociedad Zoológica de Londres (ZSL) consideran que el delfín chino de agua dulce, conocido como "baiji", está prácticamente extinguido, lo que lo convertiría en el el primer cetáceo que desaparece de la Tierra como resultado directo de la influencia del hombre. La causa; la pesca desregulada.

<p>Así lo afirman dichos expertos en un estudio publicado este miércoles en la Royal Society Biology Letters , donde aseguran que no pudieron localizar a ningún delfín de esta especie en el río Yangtze, su hábitat natural, durante una profunda investigación que duró seis semanas.</p>

<p>Especie única En los años cincuenta del siglo pasado, la población de este delfín, una
especie única del Yangtze, era de miles, pero disminuyó con los años mientras China se modernizaba y empezaba a utilizar el río para la pesca, el transporte y la generación de electricidad, añade los científicos.
La extinción representa la desaparición de una rama completa del árbol de la evolución de la vida
Sam Turvey, del ZSL y uno de los autores del análisis, calificó la extinción de "trágica". "El delfín del río Yangtze era un</p>
<p>mamífero increíble que se separó de otras especies hace más de 20 millones de años".</p>

"La extinción representa la desaparición de una rama completa del árbol de la evolución de la vida y hay que poner énfasis en que tenemos que asumir la responsabilidad sobre nuestra labor como guardianes del planeta", añadió.

Los ejemplares de esta especie, conocida científicamente como 'Lipotes vexillifer', tienen un pico largo y estrecho y viven en grupos de tres o cuatro.

Estudio visual y acústico

El equipo llevó a cabo un estudio visual y acústico durante seis semanas a finales del 2006. Si bien es posible que algún delfín no pudiera ser hallado durante la investigación, "nuestra imposibilidad de detectar un "baiji" a pesar de esta investigación intensa indica que las posibilidades de encontrarlos y llevarlos (a una reserva) han desaparecido prácticamente", indicaron los expertos.

No obstante, el Fondo Mundial para la Vida Silvestre (WWF) considera que el análisis no es definitivo. "El WWF no cree que el delfín 'baiji' pueda ser declarado extinguido o 'extinguido de hecho' porque la investigación fue realizada en un corto periodo de tiempo en un área limitada del río", añadió un portavoz de la organización</center></font>
